double * conga_reverse_Rot_Vec(double * a, double *r)
{
r[0] = a[0];r[1] = a[1];r[2] = a[2];r[3] = a[3] * -1;
return r;
}

double * conga_involute_Rot_Vec(double * a, double * r)
{
r[0] = a[0] * -1;r[1] = a[1] * -1;r[2] = a[2] * -1;r[3] = a[3] * -1;
return r;
}

double * conga_conjugate_Rot_Vec(double * a, double * r)
{
r[0] = a[0] * -1;r[1] = a[1] * -1;r[2] = a[2] * -1;r[3] = a[3];
return r;
}
	

	